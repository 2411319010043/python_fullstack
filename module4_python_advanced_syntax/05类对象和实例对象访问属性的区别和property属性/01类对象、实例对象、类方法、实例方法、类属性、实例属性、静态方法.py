'''
一、类属性和实例属性的区别
    类中的变量叫做类属性，类属性属于类 实例属性共用的属性保存在类中叫做类属性 可以节省内存空间
    通过类实例出来的叫做对象，对象中的变量叫做实例属性，实例属性属于对象 实例属性是每个对象独有的 

二、类方法和实例方法和静态方法的区别
    调用类方法要默认传入类引用
    调用实例方法默认要传入对象的引用
    调用静态方法默认什么都不用传入

    实例对象可以有多个 类对象只有一个 类中的属性和方法都是共享的 只有实例对象中的实例属性是独有的

    实例对象可以调用 类方法 实例方法 静态方法
    类对象只能调用 类方法和静态方法
'''

